package com.github.kwilinsi.jda.command.manager.tools;

/**
 * This is just a small reference class containing some of the emojis used in Discord. These are emojis that I found
 * useful in relation to the commands library.
 * <p><br>
 * Each constant is given the name that Discord assigns it, and it is set to the unicode version of that emoji.
 */
@SuppressWarnings("unused")
public class Emojis {
    /**
     * {@code :arrow_left:} symbolized ⬅
     */
    public static final String ARROW_LEFT = "\u2B05";

    /**
     * {@code :arrow_right:} symbolized ➡
     */
    public static final String ARROW_RIGHT = "\u27A1";

    /**
     * {@code :arrow_backward:} symbolized ◀
     */
    public static final String ARROW_BACKWARD = "\u25C0";

    /**
     * {@code :arrow_forward:} symbolized ▶
     */
    public static final String ARROW_FORWARD = "\u25B6";
}
